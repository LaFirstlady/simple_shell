/**
 * _erratoi - A STRING TO AN INTERGER CONVERTED
 * @s: A STRING TO BE CONVERTED
 * Return: 0 IF NO NUMBERS IN STRING, CONVERTED NUMBER OTHERWISE
 *       -1 ON ERROR
 */
int _erratoi(char *s)
{
	int i = 0;
	unsigned long int result = 0;

	if (*s == '+')
		s++;  /* TODO: why does this make main return 255? */
	for (i = 0;  s[i] != '\0'; i++)
	{
		if (s[i] >= '0' && s[i] <= '9')
		{
			result *= 10;
			result += (s[i] - '0');
			if (result > INT_MAX)
				return (-1);
		}
		else
			return (-1);
	}
	return (result);
}

/**
 * print_error - ERROR MESSAGE PRINTED
 * @info: RETURN INFO STRUCT AND THE PARAMETER
 * @estr: STRING CONTAINING SPECIFIC ERROR TYPE
 * Return: 0 IF NO NUBER IN STRING, CONVERTED NUMBER OTHERWISE
 *         -1 ON ERROR
 */
void print_error(info_t *info, char *estr)
{
	_eputs(info->fname);
	_eputs(": ");
	print_d(info->line_count, STDERR_FILENO);
	_eputs(": ");
	_eputs(info->argv[0]);
	_eputs(": ");
	_eputs(estr);
}

/**
 * print_d - FUNCTION PRINTS A DECIMAL IINTEGER NUMBER BASE 10
 * @input: INPUT
 * @fd: FILEDESCRIPTOR TO WRITE TO
 *
 * Return: NUMBER OF CHARACTERS PRINTED
 */
int print_d(int input, int fd)
{
	int (*__putchar)(char) = _putchar;
	int i, count = 0;
	unsigned int _abs_, current;

	if (fd == STDERR_FILENO)
		__putchar = _eputchar;
	if (input < 0)
	{
		_abs_ = -input;
		__putchar('-');
		count++;
	}
	else
		_abs_ = input;
	current = _abs_;
	for (i = 1000000000; i > 1; i /= 10)
	{
		if (_abs_ / i)
		{
			__putchar('0' + current / i);
			count++;
		}
		current %= i;
	}
	__putchar('0' + current);
	count++;

	return (count);
}

/**
 * convert_number - A CONVERTER FUNCTION, A CLONE OF ITOA
 * @num: NUMBER
 * @base: BASE
 * @flags: ARGUMENT FLAGS
 *
 * Return: STRING
 */
char *convert_number(long int num, int base, int flags)
{
	static char *array;
	static char buffer[50];
	char sign = 0;
	char *ptr;
	unsigned long n = num;

	if (!(flags & CONVERT_UNSIGNED) && num < 0)
	{
		n = -num;
		sign = '-';

	}
	array = flags & CONVERT_LOWERCASE ? "0123456789abcdef" : "0123456789ABCDEF";
	ptr = &buffer[49];
	*ptr = '\0';

	do	{
		*--ptr = array[n % base];
		n /= base;
	} while (n != 0);

	if (sign)
		*--ptr = sign;
	return (ptr);
}

/**
 * remove_comments - THE FIRST INSTANCE OF '#' REPLACED WITH '\0'
 * @buf: ADDRESS OF THE STRING TO MODIFY
 * Return: ALWAYS 0 (SUCCESS)
 */
void remove_comments(char *buf)
{
	int i;

	for (i = 0; buf[i] != '\0'; i++)
		if (buf[i] == '#' && (!i || buf[i - 1] == ' '))
		{
			buf[i] = '\0';
			break;
		}
}
